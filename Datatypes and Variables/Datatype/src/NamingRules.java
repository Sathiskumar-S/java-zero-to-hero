import java.util.Scanner;
public class NamingRules {
    public static void main(String[] args) {
        //1. Case sensitive
        int amount;
        float amounT;
        //2. Contains Alphabets, Numbers, _ or $
        char name1_student$no;
        //3. Start with Alphabets, _ or $
        float $name;
        int student_name;
        //4. Should not be a keyword
//        float

        //5. No limit on length of name
        int student_1$rollno_of_CSE$STudent;
        //6. Should not be the class name
        int Scanner;

        //7. Follow Camel cases
        double studentName;
    }
}
